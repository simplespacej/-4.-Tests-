def merge_mentors_lists(mentors):
    return sum(mentors, [])

def extract_first_names(mentors):
    return [mentor.split()[0] for mentor in mentors]

def get_unique_names(names):
    return sorted(set(names))

def count_names(names):
    unique_names = get_unique_names(names)
    return [[name, names.count(name)] for name in unique_names]

def get_top_n_names(names_count, n=3):
    names_count.sort(key=lambda x: x[1], reverse=True)
    return names_count[:n]

